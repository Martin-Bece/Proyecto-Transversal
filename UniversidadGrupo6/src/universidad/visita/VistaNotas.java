    private void CargarCombo(){
        for (Alumno aux : alumnos) {
            ComboAlumnos.addItem(aux);
        }
    }
    
    private void CargarModelo(){
        ArrayList<Object> columnas = new ArrayList();
        
        columnas.add("Codigo");
        columnas.add("Nombre");
        columnas.add("Nota");
        
        for (Object aux : columnas) {
            model.addColumn(aux);
        }
        
        TablaNotas.setModel(model);
    }
    
    private void borrarFilas(){
        model.setRowCount(0);
    }
    
    private void CargarMaterias(){
        Alumno aluSel = (Alumno)ComboAlumnos.getSelectedItem();
        inscripciones = id.obtenerInscripcionesPorAlumno(aluSel.getIdAlumno());
        
        for (Inscripcion aux : inscripciones) {
            model.addRow(new Object[] {aux.getMateria().getIdMateria(), aux.getMateria().getNombre(), aux.getNota()});
        }
        
    }
