    private void BotonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonGuardarActionPerformed
        // TODO add your handling code here:
        
        int fila = TablaNotas.getSelectedRow();
        
        if (fila != -1) {
             Alumno select = (Alumno) ComboAlumnos.getSelectedItem();
        
            int idMateria = (Integer) TablaNotas.getValueAt(fila, 0);
            int idAlumno = select.getIdAlumno();
            
            Object valor = TablaNotas.getValueAt(fila,2);
            double notanueva = 0;
            
            if (valor instanceof Number) {
                notanueva = ((Number) valor).doubleValue();
            } else if (valor instanceof String) {
                try {
                    notanueva = Double.parseDouble((String) valor); 
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(this, "La nota ingresada no es un valor válido", "Error", JOptionPane.ERROR_MESSAGE);
                    return; 
            }
            } else {
                JOptionPane.showMessageDialog(this, "La nota ingresada no es un valor válido", "Error", JOptionPane.ERROR_MESSAGE);
                return;  // Salimos del método si no es un tipo manejable
            }
        
                id.actualizarNota(idAlumno, idMateria, notanueva);
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione una fila para modificar la nota","Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_BotonGuardarActionPerformed


